void MoveFire() {
	int i, j, cnt;
	for (i = 0; i < FIREN; i++) {
		if (fire[i].start_x != -1) {
			fire[i].speed_count++;
			cnt = 100 / fire[i].speed;
			if (fire[i].speed_count >= cnt) {
				fire[i].speed_count = 0;
				if (!fire[i].who) {
					fire[i].current_y -= 1;
				}
				else {
					fire[i].current_y += 1;
				}
				if (fire[i].current_y <= 0 || fire[i].current_y > 47) {
					fire[i].start_x = -1;
				}
			}
		}
		if (fire[i].start_x != -1) {

			if (!fire[i].who) {
				Nokia5110_PrintBMP(0 + fire[i].current_x, fire[i].current_y, Laser0, 0);
				// Enemy killed?
				for (j = 0; j < 5; j++) {
					if (enemies.enemy[j].live) {
						if ((enemies.shift_y + ENEMY30H + 2 >= fire[i].current_y 
							|| enemies.shift_y + ENEMY30H + 4 >= fire[i].current_y + LASERH)
						&& fire[i].current_x >= enemies.shift_x + j * 16 + 2
						&& fire[i].current_x + LASERW <= enemies.shift_x + j * 16 + ENEMY30W - 2) {
							enemies.enemy[j].live = 0;
							fire[i].start_x = -1;
							Sound_Explosion();
							game.score += 100;
							checkNextLevel();
						}
					}
				}
			}
			else {
				Nokia5110_PrintBMP(0 + fire[i].current_x, fire[i].current_y, Missile0, 0);
				// You killed?
				if (fire[i].current_y >= 47
				&& fire[i].current_x >= ADCdata
				&& fire[i].current_x + LASERW <= ADCdata + PLAYERW) {
					Game_Over();
				}
			}
		}
	}
}

void FireAnimation() {
	int i;
	int fire = 0;
	for (i = 0; i < 5; i++) {
		if (enemies.enemy[i].fire) {
			enemies.enemy[i].fire_count++;
			if (enemies.enemy[i].fire_count >= enemies.enemy[i].fire_time) {
				enemies.enemy[i].fire = 0;
			}
		}
		if (enemies.enemy[i].fire) {
			fire = 1;
		}
	}
	if (fire) {
		GPIO_PORTB_DATA_R |= 0x10; 
	}
	else {
		GPIO_PORTB_DATA_R &= ~0x10; 
	}
}

void MoveEnemy() {
	if (enemies.counter_x > enemies.slow_x) {
		enemies.shift_x += enemies.side;
		enemies.counter_x = 0;
	}
	if (enemies.shift_x >= 4) {
		enemies.side = -1;
	}
	if (enemies.shift_x <= 0) {
		enemies.side = 1;
	}
	enemies.counter_x++;


	if (enemies.counter_y > enemies.slow_y) {
		enemies.shift_y++;
		enemies.counter_y = 0;
	}
	if (enemies.shift_y >= 47 - PLAYERH * 2 + 1) {
		enemies.shift_y = 0;
		Sound_Explosion();
		Game_Over();
	}
	enemies.counter_y++;
}

void EnemyFire() {
	int i;
	for (i = 0; i < 5; i++) {
		if (enemies.enemy[i].live) {
			if (Random() % 1000 == 1) {
				startFire(i + 1);
			}
		}
	}
}

