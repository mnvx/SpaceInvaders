void DAC_Init(void) {
	unsigned long volatile delay;
  SYSCTL_RCGC2_R |= SYSCTL_RCGC2_GPIOB; // activate port B
  delay = SYSCTL_RCGC2_R;    // allow time to finish activating
  GPIO_PORTB_AMSEL_R &= ~0x0F;      // no analog
  GPIO_PORTB_PCTL_R &= ~0x0000FFFF; // regular GPIO function
  GPIO_PORTB_DIR_R |= 0x0F;      // make PB5-0 out
	GPIO_PORTB_DR8R_R |=0x0F; //no comment PB5-0
  GPIO_PORTB_AFSEL_R &= ~0x0F;   // disable alt funct on PB5-0
  GPIO_PORTB_DEN_R |= 0x0F;      // enable digital I/O on PB5-0
}

void DAC_Out(unsigned long data) {
  GPIO_PORTB_DATA_R = data;
}

