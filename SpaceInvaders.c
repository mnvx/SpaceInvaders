/**
 * @param int who Who fire. 0 - me. 1, 2, ... - enemy number.
 */
void startFire(int who) {
	int i;
	for (i = 0; i < FIREN; i++) {
		if (fire[i].start_x == -1) {
			if (!who) {
				fire[i].start_x = ADCdata;
				fire[i].start_y = 47 - PLAYERH;
				fire[i].current_x = fire[i].start_x + PLAYERW / 2 - 1;
				fire[i].current_y = fire[i].start_y;
			}
			else {
				fire[i].start_x = fire[i].current_x = who * 16 - ENEMY30W / 2 + enemies.shift_x;
				fire[i].start_y = fire[i].current_y = enemies.shift_y + ENEMY30H;
				enemies.enemy[who-1].fire = 1;
				enemies.enemy[who-1].fire_count = 0;
			}

			fire[i].speed = 5;
			fire[i].speed_count = 0;
			fire[i].who = who;

			Sound_Shoot();
			return;
		}
	}
}

void checkFire() {
	if ((Key_In() > 1) & 1) {
		GPIO_PORTB_DATA_R |= 0x20; 
	}
	else {
		GPIO_PORTB_DATA_R &= ~0x20; 
	}

	if (((Key_In() > 1) & 1) != buttons.fire) {
		buttons.fire_counter++;
		if (buttons.fire_counter >= buttons.count) {
			buttons.fire = 1 - buttons.fire;
			buttons.fire_counter = 0;
			// if fire pressed
			if (buttons.fire) {
				startFire(0);
			}
		}
	}
	else {
		buttons.fire_counter = 0;
	}
}

int checkPause() {
	if ((Key_In() & 1) != buttons.pause) {
		buttons.pause_counter++;
		if (buttons.pause_counter >= buttons.count) {
			buttons.pause = 1 - buttons.pause;
			buttons.pause_counter = 0;
			// if pause toggled
			if (buttons.pause && game.play < 2) {
				if (!game.start) {
					game.start = 1;
					Random_Init(NVIC_ST_CURRENT_R);
				}
				game.play = 1 - game.play;
			}
			return 1;
		}
	}
	else {
		buttons.pause_counter = 0;
	}
	return 0;
}

